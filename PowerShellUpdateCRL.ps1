<#
 .SYNOPSIS
    PowerShell Update CRL

 .DESCRIPTION
    Itterate certificates for CDP and AIA extensions
    Download CRL from distribution point, if successful and new crl exist, remove caches

 .NOTES
    AUTHOR Jonas Henriksson

 .LINK
    https://github.com/J0N7E

 .NOTES
    Register scheduled task with:

    @{
        TaskName    = "PowerShell Update CRL"
        Description = 'Updates CRL for all certificates every 5 minutes and purge caches if successfull'
        TaskPath    = '\'
        Action      =
        @{
            Execute          = '.\PowerShellUpdateCRL.vbs'
            WorkingDirectory = "$($PWD.Path)"
        } | ForEach-Object {
            New-ScheduledTaskAction @_
        }
        Trigger     = New-ScheduledTaskTrigger -Once -At (Get-Date -Format "yyyy-MM-dd HH:00") -RepetitionInterval (New-TimeSpan -Minutes 5)
        Principal   = New-ScheduledTaskPrincipal -GroupId 'S-1-5-32-545'
        Settings    = New-ScheduledTaskSettingsSet -MultipleInstances Parallel
    } | ForEach-Object {
        Register-ScheduledTask @_
    }

 .NOTES
    Register event source with:

    foreach ($EventSource in @('PowerShell Update CRL'))
    {
        New-EventLog -LogName Application -Source $EventSource
    }

 .NOTES
    Debug event source:

    foreach ($EventSource in @('PowerShell Update CRL'))
    {
        #Check registered event source exist with:
        [System.Diagnostics.EventLog]::SourceExists($EventSource)

        #Check which log registered event source is registered under
        [System.Diagnostics.EventLog]::LogNameFromSourceName($EventSource,'.')

        #Remove registered event with:
        #[System.Diagnostics.EventLog]::DeleteEventSource($EventSource)
    }
#>

try
{
    #######
    # Init
    #######

    $CdpEntries = @{}
    $OcspEntries = @{}

    #######
    # Func
    #######

    function Write-Log
    {
        param
        (
            [ValidateSet('Error', 'Warning', 'Information')]
            [String]$EntryType,
            [String]$Message
        )

        # Check if powersehll is started as non interactive
        if ([Environment]::GetCommandLineArgs() | Where-Object { $_ -eq '-NonInteractive' })
        {
            Write-EventLog -LogName Application `
                           -Source 'PowerShell Update CRL' `
                           -EntryType $EntryType `
                           -EventId 1234 `
                           -Message $Message `
                           -Category 0
        }
        else
        {
            switch ($EntryType)
            {
                'Error'
                {
                    Write-Error -Message $Message
                }

                'Warning'
                {
                    Write-Warning -Message $Message
                }

                default
                {
                    Write-Verbose -Message $Message -Verbose
                }
            }
        }
    }

    #######
    # Enum
    #######

    # Itterate certificates
    foreach($Cert in (Get-Item -Path Cert:\*\My\*))
    {
        # Decode CDP extension
        $CdpUrl = (New-Object System.Security.Cryptography.AsnEncodedData(
            '2.5.29.31',
            $Cert.Extensions['2.5.29.31'].RawData

        # Get CDP URL
        )).Format($false) | Where-Object { $_ -match 'URL=(.*?)(?=$|\s\()' } | ForEach-Object { $Matches[1] }

        if (-not $CdpEntries.Contains("$CdpUrl"))
        {
            # Get issuer CN
            $Issuer = $Cert.Issuer | Where-Object { $_ -match 'CN=(.*?)(?:,|$)' } | ForEach-Object { $Matches[1] }

            $CdpEntries.Add("$CdpUrl", "$Issuer")
        }

        if (-not $OcspEntries.Contains("$CdpUrl"))
        {
            # Use CdpUrl as index between hashtables
            $OcspEntries.Add("$CdpUrl",

                # Decode AIA extension
                ((New-Object System.Security.Cryptography.AsnEncodedData(
                    '1.3.6.1.5.5.7.1.1',
                    $Cert.Extensions['1.3.6.1.5.5.7.1.1'].RawData

                # Get OCSP URL
                )).Format($false) | Where-Object { $_ -match '\(1.3.6.1.5.5.7.48.1\), Alternative Name=URL=(.*)$' } | ForEach-Object { $Matches[1] })
            )
        }
    }

    #######
    # Exec
    #######

    foreach($CdpUrl in $CdpEntries.GetEnumerator())
    {
        $Request = $null

        try
        {
            # Try request CRL
            $Request = Invoke-WebRequest -Uri "$($CdpUrl.Name)"
        }
        catch
        {
            # > $null
        }

        # Request successfull
        if ($Request -and $Request.StatusCode -eq '200')
        {
            # Get filename
            $CdpFile = $CdpUrl.Name.Substring($CdpUrl.Name.LastIndexOf('/') + 1)

            # Save crl to temp
            Set-Content -Value $Request.Content -LiteralPath "$env:TEMP\$CdpFile" -Encoding Byte

            # Check previous and downloaded crl
            foreach ($Arg in "-store ca `"$($CdpUrl.Value)`"", "`"$env:TEMP\$CdpFile`"")
            {
                # Get crl number
                $Value = [uint32]"0x$(Invoke-Expression -Command "certutil $Arg" | Where-Object { $_ -match 'CRL Number=(.*)$' } | ForEach-Object { $Matches[1] })"

                # Compare
                if (-not $NewVersion -or $Value -gt $NewVersion)
                {
                    $NewVersion = $Value
                }
                else
                {
                    $NewVersion = $null
                }
            }

            if ($NewVersion)
            {
                # Remove previous crl
                certutil -delstore ca "$($CdpUrl.Value)" > $null

                Write-Log -EntryType Information -Message "Adding new CRL `"$CdpFile`""
                certutil -addstore ca "$env:TEMP\$CdpFile" > $null

                $CdpCacheName = [Uri]::EscapeUriString($CdpUrl.Name)

                Write-Log -EntryType Information -Message "Remove CRL cache `"$CdpCacheName`""
                certutil -urlcache "$CdpCacheName" delete > $null

                # Check if OSCP entry exist
                if ($OcspEntries.ContainsKey($CdpUrl.Name))
                {
                    Write-Log -EntryType Information -Message "Remove OSCP cache `"$($OCSPEntries[$CdpUrl.Name])`""
                    certutil -urlcache "$($OCSPEntries[$CdpUrl.Name])" delete > $null
                }
            }

            # Remove crl from temp
            Remove-Item -Path "$env:TEMP\$CdpFile" -Force -ErrorAction SilentlyContinue
        }
    }
}
catch [Exception]
{
    Write-Log -EntryType Error -Message $_
    throw $_
}

